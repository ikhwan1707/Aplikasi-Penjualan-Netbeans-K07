 private void loadData() {
        model = new DefaultTableModel();
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        
        tabledistributor.setModel(model);
        model.addColumn("ID Distributor");
        model.addColumn("Nama Distributor");
        model.addColumn("Alamat Distributor");
        model.addColumn("Kota Asal");
        model.addColumn("Email");
        model.addColumn("Telepon");
        
        try{
            
            String sql = "SELECT * FROM tbldistributor";
            Connection c = Koneksi.getKoneksi();
            Statement s = c.createStatement();
            ResultSet r = s.executeQuery(sql);
            
            while(r.next()){
                //Lakukan penelusuran baris
                model.addRow(new Object[]{
                    r.getString(1),
                    r.getString(2),
                    r.getString(3),
                    r.getString(4),
                    r.getString(5),
                    r.getString(6)
                });
            }
        } catch (SQLException e) {
            System.out.append("Terjadi Error");
        } 
            
    }
    

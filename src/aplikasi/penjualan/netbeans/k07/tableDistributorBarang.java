    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        String id = txtId.getText();
        String nama = txtnama.getText();
        String alamat = txaalamat.getText();
        String kota = txtkota.getText();
        String email = txtemail.getText();
        String telepon = txttelepon.getText();
        
        if ("".equals(id) || "".equals(nama) || "".equals(alamat) || "".equals(kota) || "".equals(email) || ("".equals(telepon)))
        {
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
        }else{
            try{
                Connection c = Koneksi.getKoneksi();
                String sql = "INSERT INTO tbldistributor VALUES (?, ?, ?, ?, ?, ?)";
                PreparedStatement p = c.prepareStatement(sql);
                
                p.setString(1, id);
                p.setString(2, nama);
                p.setString(3, alamat);
                p.setString(4, kota);
                p.setString(5, email);
                p.setString(6, telepon); 
     
                p.executeUpdate();
                p.close();
                
                JOptionPane.showMessageDialog(null, "Penyimpanan Data Berhasil");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }finally{
                loadData();
                kosong();
                setEnabledFalse();
                btnadd.setEnabled(true);
                btnclose.setEnabled(true);
            }
        }
    }//GEN-LAST:event_btnsaveActionPerformed

